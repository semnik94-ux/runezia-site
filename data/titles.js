// Вспомогательная функция — получить слово по уровню (1-20)
function getTitleWord(family, type, level) {
  const words = TITLE_WORDS[family];
  if (!words) return null;
  var idx = 0;
  if (level >= 20) idx = 4;
  else if (level >= 15) idx = 3;
  else if (level >= 10) idx = 2;
  else if (level >= 5) idx = 1;
  return words[type][idx];
}

// Основная функция генерации — 3 варианта
function generateTitleVariants(runeFamilies, runeLevels) {
  if (!runeFamilies || runeFamilies.length === 0) return [];

  var variants = [];

  if (runeFamilies.length === 1) {
    // Одна руна — два варианта из её семейства
    var fam = runeFamilies[0];
    var lvl = runeLevels && runeLevels[0] ? runeLevels[0] : 10;
    for (var v = 0; v < 3; v++) {
      var adj1 = getTitleWord(fam, 'adj', lvl);
      var noun1 = getTitleWord(fam, 'noun', lvl);
      var adj2 = getTitleWord(fam, 'adj', Math.min(20, Math.max(1, lvl + (v - 1) * 5)));
      var noun2 = getTitleWord(fam, 'noun', Math.min(20, Math.max(1, lvl + (v - 2) * 4)));
      var baseOptions = [
        adj1 + ' ' + noun1,
        adj2 + ' ' + noun1,
        adj1 + ' ' + noun2,
      ];
      variants.push(baseOptions[v % baseOptions.length]);
    }
  } else if (runeFamilies.length >= 2) {
    var fam1 = runeFamilies[0];
    var fam2 = runeFamilies[1];
    var lvl1 = runeLevels && runeLevels[0] ? runeLevels[0] : 10;
    var lvl2 = runeLevels && runeLevels[1] ? runeLevels[1] : 10;

    var a1 = getTitleWord(fam1, 'adj', lvl1);
    var n1 = getTitleWord(fam1, 'noun', lvl1);
    var a2 = getTitleWord(fam2, 'adj', lvl2);
    var n2 = getTitleWord(fam2, 'noun', lvl2);

    // Synergy title if available
    var key1 = fam1 + '+' + fam2;
    var key2 = fam2 + '+' + fam1;
    var synergy = SYNERGY_TITLES[key1] || SYNERGY_TITLES[key2] || null;

    var options = [];
    // 1: adj1 + noun2
    options.push(a1 + ' ' + n2);
    // 2: adj2 + noun1
    options.push(a2 + ' ' + n1);
    // 3: synergy or adj1+noun1 or adj2+noun2
    if (synergy) {
      options.push(synergy);
    } else {
      options.push(a1 + ' ' + n1);
    }

    variants = options.slice(0, 3);
  }

  // Add random prefix and suffix to each variant
  var prefix = TITLE_PREFIXES[Math.floor(Math.random() * TITLE_PREFIXES.length)].name;
  var suffix = TITLE_SUFFIXES[Math.floor(Math.random() * TITLE_SUFFIXES.length)].name;

  return variants.map(function(v) {
    return {
      full: prefix + ' ' + v + ' ' + suffix,
      base: v,
      prefix: prefix,
      suffix: suffix,
    };
  });
}
